# python里支持很多算数运算符
# +加 -减 *乘 /除 **幂运算 //整除 % 取余
print(1 + 1)
print(4 - 2)
print(3 * 2)

# python3里，两个整数相除，得到的结果会是一个浮点数
print(6 / 2)  # 3.0
print(9 / 2)  # 4.5

print(3 ** 3) # 27
print(81 ** 1 / 2)  # 40.5 注意运算顺序
print(81 ** (1 / 2)) # 9.0 开平方根 **(1/2)  9.0

print(10 / 3)  # 3.3333333333333335
print(10 // 3)  # 3 整除，向下取整
print(-5 // 2)
print(10 % 3)  # 1 取余， 只取余数部分
print(1928 % 9876)  # 1928
