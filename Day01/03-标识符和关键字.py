# 标识符： 变量， 模块名， 函数名， 类名
# 标识符的命名规则与规范：
# 规则：
# 1. 由数字、字母和_组成，不能以数字开头
# 2. 严格区别大小写（计算机编程里，一共有52个英语字母）
# 3. 不能使用关键字
#    关键字：在python语言里有特殊含义的单词，例如if/for/else/while/try等等...
# 规范：建议遵守，遵守规范会显得专业，并且代码易读
# 1. 顾名思义
# 2. 小驼峰命名法：第一个单词的首字母小写，以后每个单词的首字母都大写 userNameAndPassword
# 3. 大驼峰命名法：每个单词的首字母都大写 PersonModel
# 4. 使用下划线连接： user_name_and_password
# 在python里的变量、函数、模块名使用下划线连接；python里的类名使用大驼峰命名法

